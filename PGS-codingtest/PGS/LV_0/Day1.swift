import Foundation
//MARK: 두 수의 합
func solution(_ num1: Int, _ num2: Int) -> Int {
	return num1 + num2
}

//MARK: 두 수의 차
func solution(_ num1: Int, _ num2: Int) -> Int {
	return num1 - num2
}

//MARK: 두 수의 곱
func solution(_ num1: Int, _ num2: Int) -> Int {
	return num1 * num2
}

//MARK: 몫 구하기
func solution(_ num1: Int, _ num2: Int) -> Int {
	return num1 / num2
}
